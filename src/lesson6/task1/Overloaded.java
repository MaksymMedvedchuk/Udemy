package lesson6.task1;

public class Overloaded {


    public int printSumNumber() {
        return 0;
    }

    public int printSumNumber(int Number10) {
        return Number10;
    }


    public int printSumNumber(int Number1, int Number2) {
        return Number1 + Number2;
    }

    public int printSumNumber(int Number3, int Number4, int Number5) {

        return Number3 + Number4 + Number5;
    }

    public int printSumNumber(int Number6, int Number7, int Number8, int Number9) {
        return Number6 + Number7 + Number8 + Number9;
    }


}
